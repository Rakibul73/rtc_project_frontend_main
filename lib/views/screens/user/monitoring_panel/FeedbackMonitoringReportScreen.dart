  Future<bool> _getDataAsync() async {
    if (widget.monitoringReportID.isNotEmpty) {
      await Future.delayed(const Duration(seconds: 1), () async {
        _formData.monitoringReportID = widget.monitoringReportID;

        int monitoringReportID = int.parse(widget.monitoringReportID);
        final projectMonitoringReportDetails = await ApiService.getSpecificProjectMonitoringReport(
          monitoringReportID,
        );

        if (projectMonitoringReportDetails['statuscode'] == 401) {
          // Handle token expiration
          final dialog = AwesomeDialog(
            context: context,
            dialogType: DialogType.error,
            desc: "Token expired. Please login again.",
            width: kDialogWidth,
            btnOkText: 'OK',
            btnOkOnPress: () {
              GoRouter.of(context).go(RouteUri.logout);
            },
          );
          dialog.show();
        }

        _formData.projectID = projectMonitoringReportDetails['monitoring_report_data']['ProjectID'].toString();

        _formData.reportDate = projectMonitoringReportDetails['monitoring_report_data']['ReportDate'];

        _formData.reportFileLocation = projectMonitoringReportDetails['monitoring_report_data']['ReportFileLocation'] ?? '';

        final projectRTCCodeAndTitleOnly = await ApiService.getSpecificProjectRTCCodeAndTitleOnly(
          int.parse(_formData.projectID),
        );
        if (projectRTCCodeAndTitleOnly.isNotEmpty) {
          _formData.projectTitle = projectRTCCodeAndTitleOnly['project']['ProjectTitle'] ?? '';
          _formData.rtcCode = projectRTCCodeAndTitleOnly['project']['CodeByRTC'].toString();
        }

        final budgetDetailsOriginal = await ApiService.fetchAllBudgetOfAProjectOriginal(
          int.parse(_formData.projectID),
        );
        if (budgetDetailsOriginal.isNotEmpty) {
          initialProjectBudgetOriginal = budgetDetailsOriginal;
        }

        final budgetDetails = await ApiService.fetchAllBudgetOfAProjectHistory(
          monitoringReportID,
        );
        if (budgetDetails.isNotEmpty) {
          initialProjectBudget = budgetDetails;
        }

        final ganttDetails = await ApiService.fetchAllGanttOfAProjectHistory(
          monitoringReportID,
        );
        if (ganttDetails.isNotEmpty) {
          initialProjectGantts = ganttDetails;
        }

        final ganttDetailsOriginal = await ApiService.fetchAllGanttOfAProjectOriginal(
          int.parse(_formData.projectID),
        );
        if (ganttDetailsOriginal.isNotEmpty) {
          initialProjectGanttsOriginal = ganttDetailsOriginal;
        }

        final userDetails = await ApiService.getSpecificProjectCreatorUserIDOnly(
          int.parse(_formData.projectID),
        );

        _formData.piUserID = userDetails['project']['CreatorUserID'].toString();

        final piDetailForMonitoringReport = await ApiService.getSpecificUserDetailsForFundApply(
          int.parse(_formData.piUserID),
        );

        _formData.piInstituteName = piDetailForMonitoringReport['user']['InstituteName'] ?? '';
        _formData.piInstituteAddress = piDetailForMonitoringReport['user']['InstituteLocation'] ?? '';
        _formData.piName = piDetailForMonitoringReport['user']['FirstName'] + ' ' + piDetailForMonitoringReport['user']['LastName'] ?? '';

        // read user id
        final userId = await storage.read(key: 'user_id');
        int userid = int.parse(userId!);
        final monitoringReportIdAndCommitteeId = {
          'ProjectMonitoringReportID': monitoringReportID,
          'MonitoringCommitteeUserID': userid,
        };
        final fetchSpecificFeedbackDetail = await ApiService.getfeedbackForSpecificCommitteeAndReport(monitoringReportIdAndCommitteeId);

        if (fetchSpecificFeedbackDetail['statuscode'] == 200 && (fetchSpecificFeedbackDetail['feedback']).length != 0) {
          _formData.projectMonitoringFeedbackID = fetchSpecificFeedbackDetail['feedback'][0]['ProjectMonitoringFeedbackID'].toString();
          _formData.monitoringReportID = fetchSpecificFeedbackDetail['feedback'][0]['ProjectMonitoringReportID'].toString();
          _formData.monitoringCommitteeUserID = fetchSpecificFeedbackDetail['feedback'][0]['MonitoringCommitteeUserID'].toString();
          _formData.projectID = fetchSpecificFeedbackDetail['feedback'][0]['ProjectID'].toString();
          _formData.observation = fetchSpecificFeedbackDetail['feedback'][0]['Observation'] ?? '';
          _formData.suggestion = fetchSpecificFeedbackDetail['feedback'][0]['Suggestions'] ?? '';
          _formData.recommendation = fetchSpecificFeedbackDetail['feedback'][0]['Recommendations'] ?? '';
          _formData.endorsement = fetchSpecificFeedbackDetail['feedback'][0]['Endorsement'] ?? '';
          _formData.monitoringFeedbackFileLocation = fetchSpecificFeedbackDetail['feedback'][0]['MonitoringFeedbackFileLocation'] ?? '';
          _formData.piCanViewOrNot = fetchSpecificFeedbackDetail['feedback'][0]['PiCanViewOrNot'].toString();
          initialFeedbackExist = true;
        }
      });
    }

    return true;
  }

  void _goSubmitFeedback(BuildContext context) {
    AppFocusHelper.instance.requestUnfocus();

    final dialog = AwesomeDialog(
      context: context,
      dialogType: DialogType.question,
      title: "Are you sure you want to submit this feedback for this monitoring report?",
      desc: "Note: This action cannot be undone.",
      width: kDialogWidth,
      btnOkText: "Yes",
      btnOkOnPress: () async {
        // here should make a api request to submit feedback
        _formKey.currentState!.save();
        // read user id
        final userId = await storage.read(key: 'user_id');
        int userid = int.parse(userId!);

        try {
          final feedbackData = {
            'ProjectMonitoringReportID': int.parse(_formData.monitoringReportID),
            'ProjectID': int.parse(_formData.projectID),
            'MonitoringCommitteeUserID': userid,
            'Recommendations': _formData.recommendation,
            'PiCanViewOrNot': 0,
            'Suggestions': _formData.suggestion,
            'Observation': _formData.observation,
            'Endorsement': _formData.endorsement,
          };

          final responseBody = await ApiService.createFeedbackSpecificMonitoringReport(feedbackData);
          if (responseBody['statuscode'] == 201) {
            // Handle success

            int feedbackId = responseBody['feedback_id'];
            final zzzz =
                await generateMonitoringReportWithFeedbackPDF(_formData, context, initialProjectBudgetOriginal, initialProjectBudget, initialProjectGanttsOriginal, initialProjectGantts, feedbackId);

            Uint8List zzzzz = await zzzz;

            final responseBodyuploadPdfFile = await ApiService.uploadPdfFile("monitoring_report_feedback", "MonitoringFeedbackID_${feedbackId}_PI_Name_${_formData.piName}.pdf", zzzzz, feedbackId);

            final dialog = AwesomeDialog(
              context: context,
              dialogType: DialogType.success,
              title: "Feedback submitted successfully",
              width: kDialogWidth,
              btnOkText: 'OK',
              btnOkOnPress: () => GoRouter.of(context).go(RouteUri.monitorthereportasmonitoringcommittee),
            );
            dialog.show();
          } else if (responseBody['msg'] == "Token has expired") {
            // Handle error

            final dialog = AwesomeDialog(
              context: context,
              dialogType: DialogType.error,
              title: "Token has expired , please login again",
              width: kDialogWidth,
              btnOkText: 'OK',
              btnOkOnPress: () {},
            );
            dialog.show();
          } else {
            // Handle error

            final dialog = AwesomeDialog(
              context: context,
              dialogType: DialogType.error,
              title: "Error submitting Feedback: ${responseBody['message']}",
              width: kDialogWidth,
              btnOkText: 'OK',
              btnOkOnPress: () {},
            );
            dialog.show();
          }
        } catch (e) {
          // Handle error

          final dialog = AwesomeDialog(
            context: context,
            dialogType: DialogType.error,
            title: "Error: $e",
            width: kDialogWidth,
            btnOkText: 'OK',
            btnOkOnPress: () {},
          );
          dialog.show();
        }
      },
      btnCancelText: "No",
      btnCancelOnPress: () {},
    );
    dialog.show();
  }

  @override
  Widget build(BuildContext context) {
    final themeData = Theme.of(context);

    return PortalMasterLayout(
      selectedMenuUri: RouteUri.projectmonitoringreport,
      body: FutureBuilder<bool>(
        initialData: null,
        future: (_future ??= _getDataAsync()),
        builder: (context, snapshot) {
          if (snapshot.connectionState == ConnectionState.waiting) {
            if (snapshot.hasData && snapshot.data!) {
              return _content(context);
            }
          } else if (snapshot.hasData && snapshot.data!) {
            return _content(context);
          }
          return Container(
            alignment: Alignment.center,
            padding: const EdgeInsets.symmetric(vertical: kDefaultPadding),
            child: SizedBox(
              height: 40.0,
              width: 40.0,
              child: CircularProgressIndicator(
                backgroundColor: themeData.scaffoldBackgroundColor,
              ),
            ),
          );
        },
      ),
    );
  }

